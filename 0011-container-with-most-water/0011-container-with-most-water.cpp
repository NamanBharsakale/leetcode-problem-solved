class Solution {
public:
    int maxArea(vector<int>& height) {
        int n = height.size();
        int maxWater = 0,lp = 0, rp = n -1;
        while(lp < rp)
        {
            int width = rp - lp;
            int ht =min(height[rp],height[lp]);
            int ans = width * ht;
            maxWater = max(maxWater,ans);

            height[lp] < height[rp] ? lp++ : rp--;
        }

        return maxWater;
}







































































































































































    
};